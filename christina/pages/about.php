                <span>Christina Lewis Halpern is the Founder and Executive Director of <a href="http://allstarcode.org">All Star Code</a>, a leading, nationally-recognized tech inclusion organization based in New York City that fosters entrepreneurial talent among high school boys of color and ensures they have the tools to succeed. All Star Code is growing to educate 2,000 students annually by 2020.</span><br>

                <p>She was recognized in 2016 as a <a href="test">"Woman of Power"</a> by the National Action Network, in 2014 as a <a href="test">White House Champion of Change for STEM Access</a> and in 2015 as an <a href="test">Echoing Green Black Male Achievement Finalist</a>. She and her work have been featured in Barron's, Fortune, Fast Company, The Root, Town Country, The Daily News and PBS among other media outlets.</p>

                <p>A sought-after writer and speaker on the future of the workplace, entrepreneurship, equity, philanthropy, culture and personal transformation, Christina has spoken at NY Ideas, Harvard Law School, The Atlantic Forum in Education, The Black Enterprise Entrepreneurship Forum, SXSW, BET Leading Women Redefined, Rev. Jesse Jackson's Wall Street Project Economic Summit, and many other venues. Christina is the author of <a href="test">"Lonely at the Top,"</a> a 2012 best-selling Kindle Single memoir about her father, Reginald F. Lewis, the first African-American to build a billion-dollar business.</p>

                <p>Christina is a member of the board of the <a href="test">Reginald F. Lewis Foundation</a>, and Chair of the Class of 2002 Associates Committee for Harvard College. She is also an angel investor and an advisor to other entrepreneurs.</p>

                <p>Prior to founding All Star Code, Christina worked for five years as a staff writer for the Wall Street Journal. She began her career as a crime reporter in Stamford, Conn.</p>

                <p>Christina has a BA from Harvard College in History and Literature and is passionate about catalyzing change. She lives in New York City with her husband and two children.</p>

